export function getCountryCode(countryIdentifier: string): string | undefined {
  if (!countryIdentifier) return undefined;

  const normalizedIdentifier = countryIdentifier.toLowerCase().replace(/-/g, ' ');

  // Direct match for 2-letter code
  if (normalizedIdentifier.length === 2 && Object.values(countryNameToCode).includes(normalizedIdentifier.toUpperCase())) {
    return normalizedIdentifier.toUpperCase();
  }
  
  // Match for full country name
  return countryNameToCode[normalizedIdentifier];
} 
 
 
 
 
 
 